let x = 10;
console.log("initial value of x:",x);

// using+=
x += 5
console.log("after x+=5:",x);

// using-=
x -= 3
console.log("after x-=3:",x);

// using*=
 x *= 2
console.log("after x*-=4:",x);

// using/=
x /= 2
console.log("after x/=12:",x);

// using%=
x %= 2
console.log("after x%=2:",x);





